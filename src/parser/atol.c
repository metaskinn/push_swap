#include "push_swap.h"

long	ft_atol(char *str)
{
	long	result;
	int		sign;
	int		i;

	result = 0;
	sign = 1;
	i = 0;
	if (!str)
		return (error());
	while (is_wspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	if (str[i] < '0' || str[i] > '9')
		return (error());
	result = parse_number(str, &i, sign);
	while (is_wspace(str[i]))
		i++;
	if (str[i] != '\0')
		return (error());
	return (result * sign);
}
